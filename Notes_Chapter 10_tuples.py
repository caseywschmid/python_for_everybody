# Tuples are like a reductionist version of a list. They are denoted with ()
# instead of [].

x = ('Glenn', 'Sally', 'Joseph')

# Tuples are not changeable like lists are, so once you make it, it stays like
# that forever. 
# You cannot .sort(), .append(), or .reverse() with tuples They
# are really there for efficiency.
# You can assign a tuple like this (they don't have to be limited to two things): 

(x, y) = (4, 'Fred')

